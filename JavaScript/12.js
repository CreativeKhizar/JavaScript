// Booleans and Comparision operators

// booleans are of two values
// true or false

let num1 = 5;
let num2 = 7;

console.log(num1 > num2);
console.log(num1 >= num2);
console.log(num1 < num2);
console.log(num1 <= num2);

// == vs ===

//== checks for value not for datatype
num1=7;
num2=7;
console.log(num1 == num2);
num3="7";
console.log(num1 == num3);

// === checks for both value and datatype

console.log(num1 === num3);

num1=7;
num2="7";

// != vs !==

console.log(num1 != num2);
console.log(num1 !== num2);